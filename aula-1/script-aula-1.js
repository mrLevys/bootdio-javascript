//--------------- O que é o JS? ----------------// 
// // // // // // // // // // // // // // // // // 
/*
  O JS é uma linguagem de programação de alto nível, que integra o desenvolvimento de apps e páginas web.
  É uma das linguagens de programação mais usadas no mundo do desenvolvimento.
*/
// // // // // // // // // // // // // // // // // 
//---------------- Como Surgiu? ----------------// 
// // // // // // // // // // // // // // // // // 
/*
  Em 1995 o programador Brendan Eich, que tralhava na Netscape, implementou uma linguagem de programação chamada Mocha, depois o nome mudou para LiveScript, mas na época a linguagem Java era popular, então mudaram LiveScript para JavaScript como jogada de marketing.
  A confusão entre JavaScript e Java é culpa dos marketeiros.
*/
// // // // // // // // // // // // // // // // // 
//------------- Pra que serve JS? --------------// 
// // // // // // // // // // // // // // // // // 
/*
  Para criar scripts dinâmicos que realizam a interação de apps ou páginas web.
  Imagina que o HTML é a estrutura, o CSS é o design, o JavaScript é a parte interativa do desenvolvimento.
*/
